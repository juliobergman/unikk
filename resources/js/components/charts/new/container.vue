<template>
    <v-main>
        <v-toolbar dense class="mb-4" elevation="2" rounded>
            <v-btn-toggle
                mandatory
                v-model="currentRoute"
                style="margin-left: -16px;"
            >
                <v-tooltip bottom v-for="item in chartMenu" :key="item.title">
                    <template v-slot:activator="{ on, attrs }">
                        <v-btn
                            :value="item.to"
                            :to="{ name: item.to }"
                            v-bind="attrs"
                            v-on="on"
                        >
                            <v-icon>{{ item.icon }}</v-icon>
                        </v-btn>
                    </template>
                    <span>{{ item.title }}</span>
                </v-tooltip>
            </v-btn-toggle>
            <!-- Left Side Finish -->
            <v-spacer></v-spacer>
            <!-- Right Side Start -->
            <v-btn @click="addDataSet()" text color="grey darken-3">
                <v-icon class="ml-2">mdi-chart-box-plus-outline</v-icon>
            </v-btn>
            <v-btn @click="saveChart()" icon color="grey darken-3">
                <v-icon class="ml-2">mdi-content-save</v-icon>
            </v-btn>
        </v-toolbar>
        <v-card elevation="2">
            <router-view
                :bus="bus"
                v-on:savechart.capture="storeChart($event)"
            ></router-view>
        </v-card>
    </v-main>
</template>

<script>
export default {
    props: ["bus"],
    data: () => ({
        currentRoute: null,
        chartMenu: [
            {
                title: "New Bar Chart",
                icon: "mdi-chart-bar",
                to: "newbarchart"
            },
            {
                title: "New Line Chart",
                icon: "mdi-chart-areaspline-variant",
                to: "newlinechart"
            },
            {
                title: "New Pie Chart",
                icon: "mdi-chart-pie",
                to: "newpiechart"
            }
        ]
    }),
    methods: {
        storeChart(data) {
            if (!this.$route.params.id) {
                axios
                    .post("chart/store", data)
                    .then(response => {})
                    .catch(error => {
                        console.log(error);
                    });
            }

            if (this.$route.params.id) {
                axios
                    .put("chart/" + this.$route.params.id, data)
                    .then(response => {})
                    .catch(error => {
                        console.log(error);
                    });
            }
        },
        addDataSet() {
            this.bus.$emit("addDataSet");
        },
        saveChart() {
            this.bus.$emit("saveChart");
        },
        saveUpdatedChart() {
            this.bus.$emit("saveUpdatedChart");
        },
        updateChart() {
            this.bus.$emit("updateChart");
        },
        updateOptions() {
            this.bus.$emit("updateOptions");
        }
    },
    mounted() {
        if (this.$route.name === "newchart") {
            this.$router.push({ name: "newbarchart" });
        }
        this.currentRoute = this.$route.name;
    }
};
</script>

